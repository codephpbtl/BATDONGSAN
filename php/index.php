    <script>
        // Lấy dữ liệu từ PHP sử dụng XMLHttpRequest
        var xhr = new XMLHttpRequest();
        xhr.onreadystatechange = function () {
        if (xhr.readyState === XMLHttpRequest.DONE) {
        if (xhr.status === 200) {
        var data = JSON.parse(xhr.responseText);
        console.log(data);
        drawChart(data);
        } else {
        console.error("Error fetching data");
        }
        }
        };
        xhr.open("GET", "getChartData.php", true);
        xhr.send();

        // Hàm để vẽ biểu đồ
        function drawChart(data) {
        var labels = data.map(function (item) {
        return item.Status;
        });

        var totalPriceSum = data.map(function (item) {
        return item.TotalPriceSum;
        });

        var ctx = document.getElementById("rentalsChart").getContext("2d");
        var chart = new Chart(ctx, {
        type: "bar",
        data: {
        labels: labels,
        datasets: [{
        label: "Tổng Hóa Đơn",
        data: totalPriceSum,
        backgroundColor: ["green", "red"], // Màu của cột
        borderColor: ["green", "red"], // Màu của đường viền cột
        borderWidth: 1
        }]
        },
        options: {
        scales: {
        y: {
        beginAtZero: true
        }
        }
        }
        });
        }
    </script>
    
